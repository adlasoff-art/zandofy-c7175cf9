export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => {
    const saved = localStorage.getItem("zandofy_locale");
    return (saved === "en" ? "en" : "fr") as Locale;
  });
  const [currency, setCurrencyState] = useState<CurrencyCode>(() => {
    const saved = localStorage.getItem("zandofy_currency");
    const valid: CurrencyCode[] = ["USD", "EUR", "XAF", "CDF", "NGN", "GBP", "CNY"];
    return (valid.includes(saved as CurrencyCode) ? saved : "USD") as CurrencyCode;
  });
  const [rates, setRates] = useState<Record<CurrencyCode, CurrencyInfo>>({ ...DEFAULT_CURRENCIES });
  const [cmsOverrides, setCmsOverrides] = useState<Record<string, Record<string, string>>>({});

  // Fetch dynamic exchange rates from DB
  useEffect(() => {
    supabase
      .from("exchange_rates")
      .select("target_currency, rate")
      .eq("base_currency", "USD")
      .then(({ data }) => {
        if (data && data.length > 0) {
          const updated = { ...DEFAULT_CURRENCIES };
          data.forEach((row: any) => {
            const code = row.target_currency as CurrencyCode;
            if (updated[code]) {
              updated[code] = { ...updated[code], rate: Number(row.rate) };
            }
          });
          setRates(updated);
          CURRENCIES = updated;
        }
      });
  }, []);

  // Fetch CMS text overrides from platform_settings
  useEffect(() => {
    supabase
      .from("platform_settings")
      .select("value")
      .eq("key", "cms_texts")
      .maybeSingle()
      .then(({ data }) => {
        if (data?.value && typeof data.value === "object") {
          const val = data.value as any;
          // Expected format: { fr: { "topbar.freeShipping": "...", ... }, en: { ... } }
          // or flat format: { "topbar.freeShipping": "..." } (treated as fr)
          if (val.fr || val.en) {
            setCmsOverrides(val);
          } else {
            // Flat format — treat as current locale overrides for both
            setCmsOverrides({ fr: val, en: val });
          }
        }
      });
  }, []);

  const setLocale = useCallback((l: Locale) => {
    setLocaleState(l);
    localStorage.setItem("zandofy_locale", l);
  }, []);

  const setCurrency = useCallback((c: CurrencyCode) => {
    setCurrencyState(c);
    localStorage.setItem("zandofy_currency", c);
  }, []);

  const t = useCallback(
    (key: string, params?: Record<string, string | number>) => {
      // CMS overrides take priority
      const cmsValue = cmsOverrides[locale]?.[key];
      const raw = cmsValue || translations[locale]?.[key] || translations.fr[key] || key;
      if (!params) return raw;
      // Lot 11C — interpolation simple {{name}}
      return raw.replace(/\{\{(\w+)\}\}/g, (_m, k) => {
        const v = params[k];
        return v === undefined || v === null ? "" : String(v);
      });
    },
    [locale, cmsOverrides]
  );

  const formatPrice = useCallback(
    (usdPrice: number) => {
      const info = rates[currency];
      const converted = usdPrice * info.rate;
      if (currency === "XAF" || currency === "CDF" || currency === "NGN" || currency === "CNY") {
        return `${Math.round(converted).toLocaleString()} ${info.symbol}`;
      }
      return `${info.symbol}${converted.toFixed(2)}`;
    },
    [currency, rates]
  );

  const currencySymbol = rates[currency].symbol;

  return (
    <I18nContext.Provider value={{ locale, currency, setLocale, setCurrency, t, formatPrice, currencySymbol }}>
      {children}
    </I18nContext.Provider>
  );
}

const defaultI18n: I18nContextType = {
  locale: "fr",
  currency: "USD",
  setLocale: () => {},
  setCurrency: () => {},
  t: (key: string, _params?: Record<string, string | number>) => key,
  formatPrice: (usdPrice: number) => `$${usdPrice.toFixed(2)}`,
  currencySymbol: "$",
};

export function useI18n() {
  const ctx = useContext(I18nContext);
  return ctx ?? defaultI18n;
}
